// ── Storage helpers ─────────────────────────────────────────────────────────
async function loadSettings() {
  const d = {
    theme:"light", autoClean:false, scrollBehavior:true, historyEnabled:false,
    filenameTemplate:"{title} - {date}", headerFooterStamp:true,
    pdfDefaults:{ pageSize:"A4", orientation:"portrait", marginCm:0.5 },
    scalerPresets:{}, customSelectors:{}
  };
  const s = await chrome.storage.local.get(Object.keys(d));
  return { ...d, ...s };
}
async function saveSetting(key, val) {
  await chrome.storage.local.set({ [key]: val });
}

async function loadBatches() {
  const { batches } = await chrome.storage.session.get("batches");
  return batches || {};
}
async function saveBatches(b) { await chrome.storage.session.set({ batches: b }); }

// ── Router ──────────────────────────────────────────────────────────────────
const pages = { "/settings": renderSettings, "/batches": renderBatches, "/history": renderHistory, "/selectors": renderSelectors };

function route() {
  const hash = location.hash.slice(1) || "/settings";
  document.querySelectorAll(".nav-item").forEach((n) => {
    n.classList.toggle("active", n.getAttribute("href") === "#" + hash);
  });
  const fn = pages[hash] || pages["/settings"];
  content.innerHTML = "";
  fn();
}
window.addEventListener("hashchange", route);

// Apply theme
(async () => {
  const s = await loadSettings();
  applyTheme(s.theme);
  route();
  // Check for pending batch URLs from context menu
  const { pendingBatchUrl } = await chrome.storage.session.get("pendingBatchUrl");
  if (pendingBatchUrl?.length && location.hash === "#/batches") {
    await chrome.storage.session.remove("pendingBatchUrl");
    // Will be handled in renderBatches
  }
})();

function applyTheme(t) {
  if (t === "system") t = matchMedia("(prefers-color-scheme:dark)").matches ? "dark" : "light";
  document.documentElement.dataset.theme = t;
}

